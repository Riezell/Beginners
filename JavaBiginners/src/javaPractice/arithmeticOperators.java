package javaPractice;

public class arithmeticOperators {

	public static void main(String[] args) {
		
		int x = 10;
		int y = 5;
		
		System.out.println(x+y);
		
		int a = 25;
		int b = 15;
		
		System.out.println(a-b);
		
		float c = 15.10f;
		float d = 20.60f;
		
		System.out.println(c*d);	
		
		int f = 12;
		int g = 5;
		
		x++;
		System.out.println(f);
		
		x--;
		System.out.println(g);
		
		
		
		

	}

}
